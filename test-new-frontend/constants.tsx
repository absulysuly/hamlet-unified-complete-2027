// Intl formatter
export const format = (date: Date, formatString: string) => {
    // A simple formatter for demonstration. 
    // In a real app, use a robust library like date-fns or moment.js.
    const options: Intl.DateTimeFormatOptions = {};
    if (formatString === 'd') options.day = 'numeric';
    if (formatString === 'MMM') options.month = 'short';
    if (formatString === 'h:mm a') {
        options.hour = 'numeric';
        options.minute = 'numeric';
        options.hour12 = true;
    }
    return new Intl.DateTimeFormat('en-US', options).format(date);
}

